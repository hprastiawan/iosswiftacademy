MARK: Antisipasi Risiko:
Anda sedang memimpin proyek besar yang melibatkan banyak tim. Bagaimana Anda akan mengidentifikasi potensi risiko yang mungkin terjadi selama proyek berlangsung? Apa langkah-langkah yang akan Anda ambil untuk mengantisipasi dan mengelola risiko-risiko tersebut?

