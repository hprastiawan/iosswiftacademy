MARK: Analisis Masalah:
Anda bekerja dalam tim pengembangan perangkat lunak. Suatu hari, klien Anda menghubungi Anda dengan laporan bug pada aplikasi mereka. Bagaimana Anda akan mendekati masalah ini? Apa langkah-langkah pertama yang akan Anda ambil untuk menganalisis dan memecahkan bug tersebut?

